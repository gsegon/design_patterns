

class PopcornPopper:
    """ PopcornPopper device class """

    def on(self):
        """ Turn the popcorn popper ON """
        print("Popcorn Popper is ON")

    def off(self):
        """ Turn the popcorn popper OFF """
        print('Popcorn Popper is OFF')

    def pop(self):
        """ Pop popcorn """
        print('Popping popcorn')
